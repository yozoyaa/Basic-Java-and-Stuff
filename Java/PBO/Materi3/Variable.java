/*
 * Variabel biasanya digunakan sebagai tempat/ruang di memori
 * untuk menyimpan data yang bersifat sementara
 * Variabel dapat berupa deklarasi saja
 * Variabel dapat di beri nilai atau di inisialisasikan
 */

package PBO.Materi3;

 public class Variable{

    public static void main(String[] args){
        // contoh deklarasi variabel
        String Nim;

        // contoh deklarasi sekaligus inisialisasi
        String Nama = "Hilman";
        int biayaPerSKS = 150000;
        char jk = 'L';

        // contoh deklarasi banyak variabel dengan tipe data yang sama
        byte nilAbsen, nilTugas, nilUTS, nilUAS;

        // contoh deklarasi banyak variabel sekaligus melakukan inisialisasi nilai
        byte jmlSKS = 3, totalSKS = 0;
    }
}
