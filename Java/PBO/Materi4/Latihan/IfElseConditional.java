package PBO.Materi4.Latihan;

public class IfElseConditional {
    public static void main(String args[]) {
        int angka = 5;
        if(angka < 5) {
            System.out.println("Angka lebih kecil dari 5");
        } else {
            System.out.println("Angka lebih besar dari 5");
        }
    }
}
